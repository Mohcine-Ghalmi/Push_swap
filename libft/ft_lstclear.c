#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(long))
{
	t_list	*tmp;

	if (!lst || !del)
		return ;
	tmp = *lst;
	while (*lst)
	{
		tmp = (*lst)->next;
		free(*lst);
		*lst = tmp;
	}
	free(*lst);
	free(tmp);
}
